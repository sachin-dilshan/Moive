    private void ldbTime1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ldbTime1MouseClicked
        try {
            setColors();
            ldbTime1.setBackground(Color.red);
            DefaultTableModel dtm = (DefaultTableModel) tableMovie.getModel();
            dtm.setRowCount(0);
            SheduleDTO shedule = ManageShaduleController.searchByTime(ldbTime1.getText());
            MovieDTO movie = ManageMovieController.search(shedule.getMovieID());
            CategortDTO categort = ManageCategoryController.SerchCategory(movie.getCategoryId());
            Object[] rowdata = {shedule.getTime(), shedule.getMovieID(), movie.getMoveName(), categort.getCategoryName(), movie.getStartDate(), movie.getEndDate()
            };
            dtm.addRow(rowdata);

        } catch (Exception ex) {
            Logger.getLogger(ShowMoving.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_ldbTime1MouseClicked

