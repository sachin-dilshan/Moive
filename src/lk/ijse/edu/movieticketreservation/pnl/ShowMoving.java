        String mid = (String) dtm.getValueAt(0, 1);
        String name = (String) dtm.getValueAt(0, 2);
        BookingSeet booking = new BookingSeet(mid, name);
        pnlDetails.setVisible(false);
        pnlRemove.removeAll();
